    po_id,
    COUNT(DISTINCT clr_id) AS color_count,
    COUNT(DISTINCT order_id) AS order_count
FROM daily_productions
WHERE po_id IN (
    47931,46100,51397,50477,50478,50479,50480,45953,51398,52174,51482,51483,54240,45136,45134,53942,54241,56180,56431,56033,56429,58916,58080,57942
)
GROUP BY po_id
HAVING COUNT(DISTINCT clr_id) > 1;
